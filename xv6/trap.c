// Interrupt descriptor table (shared by all CPUs).
struct gatedesc idt[256];
extern uint vectors[]; // in vectors.S: array of 256 entry pointers
struct spinlock tickslock;
uint ticks;

void tvinit(void)
{
  int i;

  for (i = 0; i < 256; i++)
    SETGATE(idt[i], 0, SEG_KCODE << 3, vectors[i], 0);
  SETGATE(idt[T_SYSCALL], 1, SEG_KCODE << 3, vectors[T_SYSCALL], DPL_USER);

  initlock(&tickslock, "time");
}

void idtinit(void)
{
  lidt(idt, sizeof(idt));
}

// Time slices for each queue level
static int time_slice[4] = {10, 20, 40, 80};

// Process state update helper function
void update_process_state(struct proc *p)
{
  if (p == 0)
    return;

  switch (p->state)
  {
  case RUNNING:
    p->cpu_burst++;
    p->end_time++; // Increment total CPU usage time

    // Check if process has used up its time quantum
    if (p->cpu_burst >= time_slice[p->q_level])
    {
      if (p->q_level < 3)
      {
        // Move to lower priority queue
        p->q_level++;
        // Reset counters
        p->cpu_burst = 0;
        p->cpu_wait = 0;
        p->io_wait_time = 0;
      }
      else
      {
        // Already at lowest queue, just reset CPU burst
        p->cpu_burst = 0;
      }
      yield(); // Force context switch
    }
    break;

  case RUNNABLE:
    p->cpu_wait++;

    // Implement aging mechanism
    if (p->cpu_wait >= 250 && p->q_level > 0)
    {
      // Move to higher priority queue
      p->q_level--;
      // Reset counters
      p->cpu_burst = 0;
      p->cpu_wait = 0;
      p->io_wait_time = 0;
    }
    break;

  case SLEEPING:
    p->io_wait_time++;
    break;
  }
}

// PAGEBREAK: 41
void trap(struct trapframe *tf)
{
  if (tf->trapno == T_SYSCALL)
  {
    if (myproc()->killed)
      exit();
    myproc()->tf = tf;
    syscall();
    if (myproc()->killed)
      exit();
    return;
  }

  switch (tf->trapno)
  {
  case T_IRQ0 + IRQ_TIMER:
    if (cpuid() == 0)
    {
      acquire(&tickslock);
      ticks++;

      // Update process state metrics on every timer tick
      struct proc *p = myproc();
      if (p != 0 && p->pid > 2) // Skip updating for idle, init, and shell processes
      {
        update_process_state(p);
      }

      wakeup(&ticks);
      release(&tickslock);
    }
    lapiceoi();
    break;
  case T_IRQ0 + IRQ_IDE:
    ideintr();
    lapiceoi();
    break;
  case T_IRQ0 + IRQ_IDE + 1:
    // Bochs generates spurious IDE1 interrupts.
    break;
  case T_IRQ0 + IRQ_KBD:
    kbdintr();
    lapiceoi();
    break;
  case T_IRQ0 + IRQ_COM1:
    uartintr();
    lapiceoi();
    break;
  case T_IRQ0 + 7:
  case T_IRQ0 + IRQ_SPURIOUS:
    cprintf("cpu%d: spurious interrupt at %x:%x\n",
            cpuid(), tf->cs, tf->eip);
    lapiceoi();
    break;

  // PAGEBREAK: 13
  default:
    if (myproc() == 0 || (tf->cs & 3) == 0)
    {
      // In kernel, it must be our mistake.
      cprintf("unexpected trap %d from cpu %d eip %x (cr2=0x%x)\n",
              tf->trapno, cpuid(), tf->eip, rcr2());
      panic("trap");
    }
    // In user space, assume process misbehaved.
    cprintf("pid %d %s: trap %d err %d on cpu %d "
            "eip 0x%x addr 0x%x--kill proc\n",
            myproc()->pid, myproc()->name, tf->trapno,
            tf->err, cpuid(), tf->eip, rcr2());
    myproc()->killed = 1;
  }

  // Force process exit if it has been killed and is in user space.
  // (If it is still executing in the kernel, let it keep running
  // until it gets to the regular system call return.)
  if (myproc() && myproc()->killed && (tf->cs & 3) == DPL_USER)
    exit();

  // Force process to give up CPU on clock tick.
  // If interrupts were on while locks held, would need to check nlock.
  if (myproc() && myproc()->state == RUNNING &&
      tf->trapno == T_IRQ0 + IRQ_TIMER)
    yield();

  // Check if the process has been killed since we yielded
  if (myproc() && myproc()->killed && (tf->cs & 3) == DPL_USER)
    exit();
}
